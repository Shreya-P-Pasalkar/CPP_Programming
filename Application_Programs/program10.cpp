#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Function Name : CheckEvenOdd
//  Description :   Used to check if number is even or odd
//  Input :         int
//  Output :        void
//  Date :          28/04/26
//  Author :        Shreya Pramod Pasalkar
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CheckEvenOdd(int iNo)
{
    if((iNo % 2) == 0)
    {
        cout<<iNo<<" is Even\n";
    }
    else 
    {
        cout<<iNo<<" is Odd\n";
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Entry-point Function
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iNo = 0;

    cout<<"Enter Number : ";
    cin>>iNo;

    CheckEvenOdd(iNo);

    return 0;
}
#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Function Name : DisplayNumbers
//  Description :   Used to display numbers using iteration(while loop)
//  Input :         nothing
//  Output :        void
//  Date :          28/04/26
//  Author :        Shreya Pramod Pasalkar
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
void DisplayNumbers()
{
    // Loop Counter
    int iCnt = 1;

    while(iCnt != 5)
    {
        cout<<iCnt<<"\n";
        iCnt++;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Entry-point Function
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    DisplayNumbers();
    
    return 0;
}
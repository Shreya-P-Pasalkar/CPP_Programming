#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Function Name : DisplayNumbers
//  Description :   Used to display numbers using iteration(for loop)
//  Input :         nothing
//  Output :        void
//  Date :          28/04/26
//  Author :        Shreya Pramod Pasalkar
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
void DisplayNumbers()
{
    // Loop Counter
    int iCnt = 0;

    for(iCnt = 1; iCnt <= 4; iCnt++)
    {
        cout<<iCnt<<"\n";
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Entry-point Function
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    DisplayNumbers();
    
    return 0;
}